package practice.d1;

//print output using switch and break          
public class $3Switch {
public static void main(String[] args) {
	int x = 2;
	switch(x) {
	case 1 : System.out.println("red");
	break;
	case 2 : System.out.println("Orange");
	break;
	case 3 : System.out.println("Blue");
	break;
	case 4 :  System.out.println("Green");
	break;
	default : System.out.println("not Matched");
	}
}
}
